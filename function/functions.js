module.exports = {
  assignImg: function(category) {
    const rootPath = '/images/tutorials-page/';
    let imgFile = '';

    switch (category) {
      case 'html/css':
        imgFile = 'html-css.svg';
        break;
      case 'javascript':
        imgFile = 'javascript.svg';
        break;
      case 'react':
        imgFile = 'react.svg';
        break;
      case 'nodejs':
        imgFile = 'nodejs.svg';
        break;
      case 'express':
        imgFile = 'express.svg';
        break;
      case 'mongodb':
        imgFile = 'mongodb.svg';
        break;
    }

    return rootPath + imgFile;
  },

  assignColor: function(category) {
    switch (category) {
      case 'html/css':
      case 'react':
      case 'mongodb':
        return 'text-white';
      case 'express':
      case 'javascript':
      case 'nodejs':
        return 'text-dark';
    }
  },

  assignProfilePic: function() {
    let n = Math.floor(Math.random() * 26);
    return `/images/profile-pic/profile${n}.svg`;
  }
};
